// CarryDataNumbering
//
// For each PabloBlock, a contiguous CarryData area holds carry,
// and advance values that are generated in one block for use in the
// next.  For a given block, the carry data area contains the
// carries, the advances and the nested data for contained blocks,
// if any. 
// Notes:
//   (a) an additional data entry is created for each if-statement
//       having more than one carry or advance opreation within it.  This
//       additional entry is a summary entry which must be nonzero to
//       indicate that there are carry or advance bits associated with
//       any operation within the if-structure (at any nesting level).
//   (b) advancing by a large amount may require multiple advance entries.
//       the number of advance entries for an operation Adv(x, n) is
//       (n + BLOCK_SIZE - 1) / BLOCK_SIZE
//
// Examine precomputes some CarryNumbering and AdvanceNumbering, as
// well as mMaxWhileDepth of while loops.
// 
unsigned PabloCompiler::Examine(PabloBlock & blk, unsigned carryDataIndexIn) {
    // Count local carries and advances at this level.
    unsigned carryDataIndex = carryDataIndexIn;
    unsigned localCarries = 0;
    unsigned localAdvances = 0;
    unsigned nestedCarryDataSize = 0;
    for (Statement * stmt : blk) {
        if (Advance * adv = dyn_cast<Advance>(stmt)) {
            adv->setLocalAdvanceIndex(localAdvances);
            localAdvances += (adv->getAdvanceAmount() + BLOCK_SIZE - 1) / BLOCK_SIZE;
        }
        else if (MatchStar * m = dyn_cast<MatchStar>(stmt)) {
            m->setLocalCarryIndex(localCarries);
            ++localCarries;
        }
        else if (ScanThru * s = dyn_cast<ScanThru>(stmt)) {
            s->setLocalCarryIndex(localCarries);
            ++localCarries;
        }
    }
    carryDataIndex += localCarries + localAdvances;
    for (Statement * stmt : blk) {
        if (Call * call = dyn_cast<Call>(stmt)) {
            mCalleeMap.insert(std::make_pair(call->getCallee(), nullptr));
        }
        else if (If * ifStatement = dyn_cast<If>(stmt)) {
            ++mIfDepth;
            const auto ifCarryDataSize = Examine(ifStatement->getBody(), carryDataIndex);
            --mIfDepth;
            nestedCarryDataSize += ifCarryDataSize;
            carryDataIndex += ifCarryDataSize;
        }
        else if (While * whileStatement = dyn_cast<While>(stmt)) {
            mMaxWhileDepth = std::max(mMaxWhileDepth, ++mWhileDepth);
            const auto whileCarryDataSize = Examine(whileStatement->getBody(), carryDataIndex);
            --mWhileDepth;
            nestedCarryDataSize += whileCarryDataSize;
            carryDataIndex += whileCarryDataSize;
        }
    }
    blk.setCarryIndexBase(carryDataIndexIn);
    blk.setLocalCarryCount(localCarries);
    blk.setLocalAdvanceCount(localAdvances);
    unsigned totalCarryDataSize = localCarries + localAdvances + nestedCarryDataSize;
    if ((mIfDepth > 0) && (totalCarryDataSize > 1)) {
        // Need extra space for the summary variable, always the last
        // entry within an if block.
        totalCarryDataSize += 1;
    }
    blk.setTotalCarryDataSize(totalCarryDataSize);
    return totalCarryDataSize;
}

void PabloCompiler::DeclareCallFunctions() {
    for (auto mapping : mCalleeMap) {
        const String * callee = mapping.first;
        //std::cerr << callee->str() << " to be declared\n";
        auto ei = mExternalMap.find(callee->value());
        if (ei != mExternalMap.end()) {
            void * fn_ptr = ei->second;
            //std::cerr << "Ptr found:" <<  std::hex << ((intptr_t) fn_ptr) << std::endl;
            Value * externalValue = mMod->getOrInsertFunction(callee->value(), mBitBlockType, mBasisBitsInputPtr, NULL);
            if (LLVM_UNLIKELY(externalValue == nullptr)) {
                throw std::runtime_error("Could not create static method call for external function \"" + callee->to_string() + "\"");
            }
            mExecutionEngine->addGlobalMapping(cast<GlobalValue>(externalValue), fn_ptr);
            mCalleeMap[callee] = externalValue;
        }
        else {
            throw std::runtime_error("External function \"" + callee->to_string() + "\" not installed");
        }
    }
}

void PabloCompiler::compileBlock(const PabloBlock & blk) {
    for (const Statement * statement : blk) {
        compileStatement(statement);
    }
}




void PabloCompiler::compileIf(const If * ifStatement) {        
        //
        //  The If-ElseZero stmt:
        //  if <predicate:expr> then <body:stmt>* elsezero <defined:var>* endif
        //  If the value of the predicate is nonzero, then determine the values of variables
        //  <var>* by executing the given statements.  Otherwise, the value of the
        //  variables are all zero.  Requirements: (a) no variable that is defined within
        //  the body of the if may be accessed outside unless it is explicitly
        //  listed in the variable list, (b) every variable in the defined list receives
        //  a value within the body, and (c) the logical consequence of executing
        //  the statements in the event that the predicate is zero is that the
        //  values of all defined variables indeed work out to be 0.
        //
        //  Simple Implementation with Phi nodes:  a phi node in the if exit block
        //  is inserted for each variable in the defined variable list.  It receives
        //  a zero value from the ifentry block and the defined value from the if
        //  body.
        //
        BasicBlock * ifEntryBlock = mBasicBlock;  // The block we are in.
        BasicBlock * ifBodyBlock = BasicBlock::Create(mMod->getContext(), "if.body", mFunction, 0);
        BasicBlock * ifEndBlock = BasicBlock::Create(mMod->getContext(), "if.end", mFunction, 0);
        
        IRBuilder<> b_entry(ifEntryBlock);
        mBasicBlock = ifEntryBlock;
    
        const unsigned baseCarryDataIdx = ifStatement->getBody().getCarryIndexBase();
        const unsigned carryDataSize = ifStatement->getBody().getTotalCarryDataSize();
        const unsigned carrySummaryIndex = baseCarryDataIdx + carryDataSize - 1;
        
        Value* if_test_value = compileExpression(ifStatement->getCondition());
        if (carryDataSize > 0) {
            // load the summary variable
            Value* last_if_pending_data = genCarryDataLoad(carrySummaryIndex);
            if_test_value = b_entry.CreateOr(if_test_value, last_if_pending_data);
        }
        b_entry.CreateCondBr(genBitBlockAny(if_test_value), ifEndBlock, ifBodyBlock);

        // Entry processing is complete, now handle the body of the if.
        mBasicBlock = ifBodyBlock;
        compileBlock(ifStatement -> getBody());

        // If we compiled an If or a While statement, we won't be in the same basic block as before.
        // Create the branch from the current basic block to the end block.
        IRBuilder<> bIfBody(mBasicBlock);
        // After the recursive compile, now insert the code to compute the summary
        // carry over variable.
        
        if (carryDataSize > 1) {
            // If there was only one carry entry, then it also serves as the summary variable.
            // Otherwise, we need to combine entries to compute the summary.
            Value * carry_summary = mZeroInitializer;
            for (int c = baseCarryDataIdx; c < carrySummaryIndex; c++) {
                int s = mCarryDataSummaryIdx[c];
                if (s == -1) {
                    Value* carryq_value = mCarryOutVector[c];
                    if (carry_summary == mZeroInitializer) {
                        carry_summary = carryq_value;
                    }
                    else {
                        carry_summary = bIfBody.CreateOr(carry_summary, carryq_value);
                    }
                    mCarryDataSummaryIdx[c] = carrySummaryIndex;
                }
            }
            genCarryDataStore(carry_summary, carrySummaryIndex);
        }
        bIfBody.CreateBr(ifEndBlock);
        //End Block
        IRBuilder<> bEnd(ifEndBlock);
        for (const PabloAST * node : ifStatement->getDefined()) {
            const Assign * assign = cast<Assign>(node);
            PHINode * phi = bEnd.CreatePHI(mBitBlockType, 2, assign->getName()->value());
            auto f = mMarkerMap.find(assign);
            assert (f != mMarkerMap.end());
            phi->addIncoming(mZeroInitializer, ifEntryBlock);
            phi->addIncoming(f->second, mBasicBlock);
            mMarkerMap[assign] = phi;
        }
        // Create the phi Node for the summary variable.
        if (carryDataSize > 0) {
            PHINode * summary_phi = bEnd.CreatePHI(mBitBlockType, 2, "summary");
            summary_phi->addIncoming(mZeroInitializer, ifEntryBlock);
            summary_phi->addIncoming(mCarryOutVector[carrySummaryIndex], mBasicBlock);
            mCarryOutVector[carrySummaryIndex] = summary_phi;
        }
        
        // Set the basic block to the new end block
        mBasicBlock = ifEndBlock;
}

void PabloCompiler::compileWhile(const While * whileStatement) {
        const unsigned baseCarryDataIdx = whileStatement->getBody().getCarryIndexBase();
        const unsigned carryDataSize = whileStatement->getBody().getTotalCarryDataSize();
    
        if (mWhileDepth == 0) {
            for (auto i = 0; i < carryDataSize; ++i) {
                genCarryDataLoad(baseCarryDataIdx + i);
            }
        }

        SmallVector<const Next*, 4> nextNodes;
        for (const PabloAST * node : whileStatement->getBody()) {
            if (isa<Next>(node)) {
                nextNodes.push_back(cast<Next>(node));
            }
        }

        // Compile the initial iteration statements; the calls to genCarryDataStore will update the
        // mCarryOutVector with the appropriate values. Although we're not actually entering a new basic
        // block yet, increment the nesting depth so that any calls to genCarryDataLoad or genCarryDataStore
        // will refer to the previous value.

        ++mWhileDepth;

        compileBlock(whileStatement->getBody());

        // Reset the carry queue index. Note: this ought to be changed in the future. Currently this assumes
        // that compiling the while body twice will generate the equivalent IR. This is not necessarily true
        // but works for now.

        BasicBlock* whileCondBlock = BasicBlock::Create(mMod->getContext(), "while.cond", mFunction, 0);
        BasicBlock* whileBodyBlock = BasicBlock::Create(mMod->getContext(), "while.body", mFunction, 0);
        BasicBlock* whileEndBlock = BasicBlock::Create(mMod->getContext(), "while.end", mFunction, 0);

        // Note: compileBlock may update the mBasicBlock pointer if the body contains nested loops. It
        // may not be same one that we entered the function with.
        IRBuilder<> bEntry(mBasicBlock);
        bEntry.CreateBr(whileCondBlock);

        // CONDITION BLOCK
        IRBuilder<> bCond(whileCondBlock);
        // generate phi nodes for any carry propogating instruction
        std::vector<PHINode*> phiNodes(carryDataSize + nextNodes.size());
        unsigned index = 0;
        for (index = 0; index < carryDataSize; ++index) {
            PHINode * phi = bCond.CreatePHI(mBitBlockType, 2);
            phi->addIncoming(mCarryOutVector[baseCarryDataIdx + index], mBasicBlock);
            mCarryInVector[baseCarryDataIdx + index] = mZeroInitializer; // (use phi for multi-carry mode.)
            phiNodes[index] = phi;
        }
        // and for any Next nodes in the loop body
        for (const Next * n : nextNodes) {
            PHINode * phi = bCond.CreatePHI(mBitBlockType, 2, n->getName()->value());
            auto f = mMarkerMap.find(n->getInitial());
            assert (f != mMarkerMap.end());
            phi->addIncoming(f->second, mBasicBlock);
            mMarkerMap[n->getInitial()] = phi;
            phiNodes[index++] = phi;
        }

        mBasicBlock = whileCondBlock;
        bCond.CreateCondBr(genBitBlockAny(compileExpression(whileStatement->getCondition())), whileEndBlock, whileBodyBlock);

        // BODY BLOCK
        //std::cerr << "Compile loop body\n";
        mBasicBlock = whileBodyBlock;
        compileBlock(whileStatement->getBody());
        // update phi nodes for any carry propogating instruction
        IRBuilder<> bWhileBody(mBasicBlock);
        for (index = 0; index < carryDataSize; ++index) {
            PHINode * phi = phiNodes[index];
            Value * carryOut = bWhileBody.CreateOr(phi, mCarryOutVector[baseCarryDataIdx + index]);
            phi->addIncoming(carryOut, mBasicBlock);
            mCarryOutVector[baseCarryDataIdx + index] = phi;
        }
        
        // and for any Next nodes in the loop body
        for (const Next * n : nextNodes) {
            auto f = mMarkerMap.find(n->getInitial());
            assert (f != mMarkerMap.end());
            PHINode * phi = phiNodes[index++];
            phi->addIncoming(f->second, mBasicBlock);
            mMarkerMap[n->getInitial()] = phi;
        }

        bWhileBody.CreateBr(whileCondBlock);

        // EXIT BLOCK
        mBasicBlock = whileEndBlock;
        if (--mWhileDepth == 0) {
            for (index = 0; index < carryDataSize; ++index) {
                genCarryDataStore(phiNodes[index], baseCarryDataIdx + index);
            }
        }
  
}

void PabloCompiler::compileStatement(const Statement * stmt)
{
    IRBuilder<> b(mBasicBlock);
    if (const Assign * assign = dyn_cast<const Assign>(stmt)) {
        Value * expr = compileExpression(assign->getExpr());
        if (DumpTrace) {
            genPrintRegister(assign->getName()->to_string(), expr);
        }
        mMarkerMap[assign] = expr;
        if (LLVM_UNLIKELY(assign->isOutputAssignment())) {
            SetOutputValue(expr, assign->getOutputIndex());
        }
    }
    else if (const Next * next = dyn_cast<const Next>(stmt)) {
        Value * expr = compileExpression(next->getExpr());
        if (TraceNext) {
            genPrintRegister(next->getInitial()->getName()->to_string(), expr);
        }
        mMarkerMap[next->getInitial()] = expr;
    }
    else if (const If * ifStatement = dyn_cast<const If>(stmt))
    {
        compileIf(ifStatement);
    }
    else if (const While * whileStatement = dyn_cast<const While>(stmt))
    {
        compileWhile(whileStatement);
    }
    else if (const Call* call = dyn_cast<Call>(stmt)) {
        //Call the callee once and store the result in the marker map.
        auto mi = mMarkerMap.find(call);
        if (mi == mMarkerMap.end()) {
            auto ci = mCalleeMap.find(call->getCallee());
            if (LLVM_UNLIKELY(ci == mCalleeMap.end())) {
                throw std::runtime_error("Unexpected error locating static function for \"" + call->getCallee()->to_string() + "\"");
            }
            mi = mMarkerMap.insert(std::make_pair(call, b.CreateCall(ci->second, mBasisBitsAddr))).first;
        }
        // return mi->second;
    }
    else if (const And * pablo_and = dyn_cast<And>(stmt)) {
        Value * expr = b.CreateAnd(compileExpression(pablo_and->getExpr1()), compileExpression(pablo_and->getExpr2()), "and");
        if (DumpTrace) {
            genPrintRegister(stmt->getName()->to_string(), expr);
        }
        mMarkerMap[pablo_and] = expr;
        // return expr;
    }
    else if (const Or * pablo_or = dyn_cast<Or>(stmt)) {
        Value * expr = b.CreateOr(compileExpression(pablo_or->getExpr1()), compileExpression(pablo_or->getExpr2()), "or");
        if (DumpTrace) {
            genPrintRegister(stmt->getName()->to_string(), expr);
        }
        mMarkerMap[pablo_or] = expr;
        // return expr;
    }
    else if (const Xor * pablo_xor = dyn_cast<Xor>(stmt)) {
        Value * expr = b.CreateXor(compileExpression(pablo_xor->getExpr1()), compileExpression(pablo_xor->getExpr2()), "xor");
        mMarkerMap[pablo_xor] = expr;
        // return expr;
    }
    else if (const Sel * sel = dyn_cast<Sel>(stmt)) {
        Value* ifMask = compileExpression(sel->getCondition());
        Value* ifTrue = b.CreateAnd(ifMask, compileExpression(sel->getTrueExpr()));
        Value* ifFalse = b.CreateAnd(genNot(ifMask), compileExpression(sel->getFalseExpr()));
        Value * expr = b.CreateOr(ifTrue, ifFalse);
        if (DumpTrace) {
            genPrintRegister(stmt->getName()->to_string(), expr);
        }
        mMarkerMap[sel] = expr;
        // return expr;
    }
    else if (const Not * pablo_not = dyn_cast<Not>(stmt)) {
        Value * expr = genNot(compileExpression(pablo_not->getExpr()));
        if (DumpTrace) {
            genPrintRegister(stmt->getName()->to_string(), expr);
        }
        mMarkerMap[pablo_not] = expr;
        // return expr;
    }
    else if (const Advance * adv = dyn_cast<Advance>(stmt)) {
        Value* strm_value = compileExpression(adv->getExpr());
        int shift = adv->getAdvanceAmount();
        unsigned advance_index = adv->getLocalAdvanceIndex();
        Value * expr = genAdvanceWithCarry(strm_value, shift, advance_index, stmt->getParent());
        if (DumpTrace) {
            genPrintRegister(stmt->getName()->to_string(), expr);
        }
        mMarkerMap[adv] = expr;
        // return expr;
    }
    else if (const MatchStar * mstar = dyn_cast<MatchStar>(stmt))
    {
        Value * marker = compileExpression(mstar->getMarker());
        Value * cc = compileExpression(mstar->getCharClass());
        Value * marker_and_cc = b.CreateAnd(marker, cc);
        unsigned carry_index = mstar->getLocalCarryIndex();
        Value * expr = b.CreateOr(b.CreateXor(genAddWithCarry(marker_and_cc, cc, carry_index, stmt->getParent()), cc), marker, "matchstar");
        if (DumpTrace) {
            genPrintRegister(stmt->getName()->to_string(), expr);
        }
        mMarkerMap[mstar] = expr;
        // return expr;
    }
    else if (const ScanThru * sthru = dyn_cast<ScanThru>(stmt))
    {
        Value * marker_expr = compileExpression(sthru->getScanFrom());
        Value * cc_expr = compileExpression(sthru->getScanThru());
        unsigned carry_index = sthru->getLocalCarryIndex();
        Value * expr = b.CreateAnd(genAddWithCarry(marker_expr, cc_expr, carry_index, stmt->getParent()), genNot(cc_expr), "scanthru");
        if (DumpTrace) {
            genPrintRegister(stmt->getName()->to_string(), expr);
        }
        mMarkerMap[sthru] = expr;
        // return expr;
    }
    else {
        PabloPrinter::print(stmt, std::cerr);
        throw std::runtime_error("Unrecognized Pablo Statement! can't compile.");
    }
}

Value * PabloCompiler::compileExpression(const PabloAST * expr) {
    if (isa<Ones>(expr)) {
        return mOneInitializer;
    }
    else if (isa<Zeroes>(expr)) {
        return mZeroInitializer;
    }
    else if (const Next * next = dyn_cast<Next>(expr)) {
        expr = next->getInitial();
    }
    auto f = mMarkerMap.find(expr);
    if (f == mMarkerMap.end()) {
        std::stringstream str;
        str << "\"";
        PabloPrinter::print(expr, str);
        str << "\" was used before definition!";
        throw std::runtime_error(str.str());
    }
    return f->second;
}


