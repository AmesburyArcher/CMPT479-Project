Value* PabloCompiler::genAddWithCarry(Value* e1, Value* e2, unsigned localIndex, const PabloBlock * blk) {
    IRBuilder<> b(mBasicBlock);

    //CarryQ - carry in.
    const int carryIdx = blk->getCarryIndexBase() + localIndex;
    Value* carryq_value = genCarryDataLoad(carryIdx);
#ifdef USE_TWO_UADD_OVERFLOW
    //This is the ideal implementation, which uses two uadd.with.overflow
    //The back end should be able to recognize this pattern and combine it into uadd.with.overflow.carryin
    CastInst* int128_e1 = new BitCastInst(e1, b.getIntNTy(BLOCK_SIZE), "e1_128", mBasicBlock);
    CastInst* int128_e2 = new BitCastInst(e2, b.getIntNTy(BLOCK_SIZE), "e2_128", mBasicBlock);
    CastInst* int128_carryq_value = new BitCastInst(carryq_value, b.getIntNTy(BLOCK_SIZE), "carryq_128", mBasicBlock);

    SumWithOverflowPack sumpack0, sumpack1;

    sumpack0 = callUaddOverflow(int128_e1, int128_e2);
    sumpack1 = callUaddOverflow(sumpack0.sum, int128_carryq_value);

    Value* obit = b.CreateOr(sumpack0.obit, sumpack1.obit, "carry_bit");
    Value* sum = b.CreateBitCast(sumpack1.sum, mBitBlockType, "ret_sum");

    /*obit is the i1 carryout, zero extend and insert it into a v2i64 or v4i64 vector.*/
    ConstantAggregateZero* const_packed_5 = ConstantAggregateZero::get(mBitBlockType);
    ConstantInt* const_int32_6 = ConstantInt::get(mMod->getContext(), APInt(32, StringRef("0"), 10));
    CastInst* int64_o0 = new ZExtInst(obit, IntegerType::get(mMod->getContext(), 64), "o0", mBasicBlock);
    InsertElementInst* carry_out = InsertElementInst::Create(const_packed_5, int64_o0, const_int32_6, "carry_out", mBasicBlock);

#elif defined USE_UADD_OVERFLOW
    //use llvm.uadd.with.overflow.i128 or i256
    CastInst* int128_e1 = new BitCastInst(e1, b.getIntNTy(BLOCK_SIZE), "e1_128", mBasicBlock);
    CastInst* int128_e2 = new BitCastInst(e2, b.getIntNTy(BLOCK_SIZE), "e2_128", mBasicBlock);

    //get i1 carryin from iBLOCK_SIZE
    ConstantInt* const_int32_6 = ConstantInt::get(mMod->getContext(), APInt(32, StringRef("0"), 10));
    ExtractElementInst * int64_carryq_value = ExtractElementInst::Create(carryq_value, const_int32_6, "carryq_64", mBasicBlock);
    CastInst* int1_carryq_value = new TruncInst(int64_carryq_value, IntegerType::get(mMod->getContext(), 1), "carryq_1", mBasicBlock);

    SumWithOverflowPack sumpack0;
    sumpack0 = callUaddOverflow(int128_e1, int128_e2, int1_carryq_value);
    Value* obit = sumpack0.obit;
    Value* sum = b.CreateBitCast(sumpack0.sum, mBitBlockType, "sum");

    /*obit is the i1 carryout, zero extend and insert it into a v2i64 or v4i64 vector.*/
    ConstantAggregateZero* const_packed_5 = ConstantAggregateZero::get(mBitBlockType);
    CastInst* int64_o0 = new ZExtInst(obit, IntegerType::get(mMod->getContext(), 64), "o0", mBasicBlock);
    InsertElementInst* carry_out = InsertElementInst::Create(const_packed_5, int64_o0, const_int32_6, "carry_out", mBasicBlock);
#elif (BLOCK_SIZE == 128)
    //calculate carry through logical ops
    Value* carrygen = b.CreateAnd(e1, e2, "carrygen");
    Value* carryprop = b.CreateOr(e1, e2, "carryprop");
    Value* digitsum = b.CreateAdd(e1, e2, "digitsum");
    Value* partial = b.CreateAdd(digitsum, carryq_value, "partial");
    Value* digitcarry = b.CreateOr(carrygen, b.CreateAnd(carryprop, genNot(partial)));
    Value* mid_carry_in = genShiftLeft64(b.CreateLShr(digitcarry, 63), "mid_carry_in");

    Value* sum = b.CreateAdd(partial, mid_carry_in, "sum");
    Value* carry_out = genShiftHighbitToLow(BLOCK_SIZE, b.CreateOr(carrygen, b.CreateAnd(carryprop, genNot(sum))));
#else
    //BLOCK_SIZE == 256, there is no other implementation
    static_assert(false, "Add with carry for 256-bit bitblock requires USE_UADD_OVERFLOW");
#endif //USE_TWO_UADD_OVERFLOW

    genCarryDataStore(carry_out, carryIdx);
    return sum;
}
//#define CARRY_DEBUG
Value* PabloCompiler::genCarryDataLoad(const unsigned index) {
    assert (index < mCarryInVector.size());
    if (mWhileDepth == 0) {
        IRBuilder<> b(mBasicBlock);
        mCarryInVector[index] = b.CreateAlignedLoad(b.CreateGEP(mCarryDataPtr, b.getInt64(index)), BLOCK_SIZE/8, false);
    }
#ifdef CARRY_DEBUG
    genPrintRegister("carry_in_" + std::to_string(index), mCarryInVector[index]);
#endif
    return mCarryInVector[index];
}

void PabloCompiler::genCarryDataStore(Value* carryOut, const unsigned index ) {
    assert (carryOut);
    assert (index < mCarryOutVector.size());
    if (mWhileDepth == 0) {
        IRBuilder<> b(mBasicBlock);
        b.CreateAlignedStore(carryOut, b.CreateGEP(mCarryDataPtr, b.getInt64(index)), BLOCK_SIZE/8, false);
    }
    mCarryDataSummaryIdx[index] = -1;
#ifdef CARRY_DEBUG
    genPrintRegister("carry_out_" + std::to_string(index), mCarryOutVector[index]);
#endif
    mCarryOutVector[index] = carryOut;
    //std::cerr << "mCarryOutVector[" << index << "]]\n";
}

inline Value* PabloCompiler::genBitBlockAny(Value* test) {
    IRBuilder<> b(mBasicBlock);
    Value* cast_marker_value_1 = b.CreateBitCast(test, b.getIntNTy(BLOCK_SIZE));
    return b.CreateICmpEQ(cast_marker_value_1, ConstantInt::get(b.getIntNTy(BLOCK_SIZE), 0));
}

Value * PabloCompiler::genShiftHighbitToLow(unsigned FieldWidth, Value * op) {
    unsigned FieldCount = BLOCK_SIZE/FieldWidth;
    IRBuilder<> b(mBasicBlock);
    VectorType * vType = VectorType::get(IntegerType::get(mMod->getContext(), FieldWidth), FieldCount);
    Value * v = b.CreateBitCast(op, vType);
    return b.CreateBitCast(b.CreateLShr(v, FieldWidth - 1), mBitBlockType);
}

Value* PabloCompiler::genShiftLeft64(Value* e, const Twine &namehint) {
    IRBuilder<> b(mBasicBlock);
    Value* i128_val = b.CreateBitCast(e, b.getIntNTy(BLOCK_SIZE));
    return b.CreateBitCast(b.CreateShl(i128_val, 64, namehint), mBitBlockType);
}

inline Value* PabloCompiler::genNot(Value* expr) {
    IRBuilder<> b(mBasicBlock);
    return b.CreateXor(expr, mOneInitializer, "not");
}

unsigned const LongAdvanceBase = 64;
    
Value* PabloCompiler::genAdvanceWithCarry(Value* strm_value, int shift_amount, unsigned localIndex, const PabloBlock * blk) {
    if (shift_amount >= LongAdvanceBase) {
        return genLongAdvanceWithCarry(strm_value, shift_amount, localIndex, blk);
    }
    IRBuilder<> b(mBasicBlock);
    const auto advanceIndex = blk->getCarryIndexBase() + blk->getLocalCarryCount() + localIndex;
    Value* result_value;
    
    if (shift_amount == 0) {
        result_value = genCarryDataLoad(advanceIndex);
        //b.CreateCall(mFunc_print_register, result_value);
    }
#if (BLOCK_SIZE == 128) && !defined(USE_LONG_INTEGER_SHIFT)
    if (shift_amount == 1) {
        Value* advanceq_value = genShiftHighbitToLow(BLOCK_SIZE, genCarryDataLoad(advanceIndex));
        Value* srli_1_value = b.CreateLShr(strm_value, 63);
        Value* packed_shuffle;
        Constant* const_packed_1_elems [] = {b.getInt32(0), b.getInt32(2)};
        Constant* const_packed_1 = ConstantVector::get(const_packed_1_elems);
        packed_shuffle = b.CreateShuffleVector(advanceq_value, srli_1_value, const_packed_1);
        
        Constant* const_packed_2_elems[] = {b.getInt64(1), b.getInt64(1)};
        Constant* const_packed_2 = ConstantVector::get(const_packed_2_elems);
        
        Value* shl_value = b.CreateShl(strm_value, const_packed_2);
        result_value = b.CreateOr(shl_value, packed_shuffle, "advance");
    }
    else { //if (block_shift < BLOCK_SIZE) {
        // This is the preferred logic, but is too slow for the general case.
        // We need to speed up our custom LLVM for this code.
        Value* advanceq_longint = b.CreateBitCast(genCarryDataLoad(advanceIndex), b.getIntNTy(BLOCK_SIZE));
        Value* strm_longint = b.CreateBitCast(strm_value, b.getIntNTy(BLOCK_SIZE));
        Value* adv_longint = b.CreateOr(b.CreateShl(strm_longint, shift_amount), b.CreateLShr(advanceq_longint, BLOCK_SIZE - shift_amount), "advance");
        result_value = b.CreateBitCast(adv_longint, mBitBlockType);
    }
#else
    Value* advanceq_longint = b.CreateBitCast(genCarryDataLoad(advanceIndex), b.getIntNTy(BLOCK_SIZE));
    Value* strm_longint = b.CreateBitCast(strm_value, b.getIntNTy(BLOCK_SIZE));
    Value* adv_longint = b.CreateOr(b.CreateShl(strm_longint, block_shift), b.CreateLShr(advanceq_longint, BLOCK_SIZE - shift_amount), "advance");
    result_value = b.CreateBitCast(adv_longint, mBitBlockType);
    
#endif
    genCarryDataStore(strm_value, advanceIndex);
    return result_value;
}

//
// Generate code for long advances >= LongAdvanceBase
//
Value* PabloCompiler::genLongAdvanceWithCarry(Value* strm_value, int shift_amount, unsigned localIndex, const PabloBlock * blk) {
    IRBuilder<> b(mBasicBlock);
    int advEntries = (shift_amount - 1) / BLOCK_SIZE + 1;
    //int advCeil = log2ceil(advEntries);
    //Value * indexMask = b.getInt64(advCeil - 1);
    int block_shift = shift_amount % BLOCK_SIZE;
    const auto advanceIndex = blk->getCarryIndexBase() + blk->getLocalCarryCount() + localIndex;
    //Value * blockIndex = b.CreateAnd(mBlockNo, indexMask);
    //Value * idx2 = b.CreateAnd(b.CreateAdd(mBlockNo - b.getInt64(advEntries - 1)), indexMask);
    const auto storeIdx = advanceIndex;
    const auto loadIdx = advanceIndex + advEntries - 1;
    Value* result_value;

    if (block_shift == 0) {
        result_value = genCarryDataLoad(loadIdx);
    }
    else if (advEntries == 1) {
        Value* advanceq_longint = b.CreateBitCast(genCarryDataLoad(loadIdx), b.getIntNTy(BLOCK_SIZE));
        Value* strm_longint = b.CreateBitCast(strm_value, b.getIntNTy(BLOCK_SIZE));
        Value* adv_longint = b.CreateOr(b.CreateShl(strm_longint, block_shift), b.CreateLShr(advanceq_longint, BLOCK_SIZE - block_shift), "advance");
        result_value = b.CreateBitCast(adv_longint, mBitBlockType);
    }
    else {
        // The advance is based on the two oldest bit blocks in the advance queue.
        Value* advanceq_longint = b.CreateBitCast(genCarryDataLoad(loadIdx), b.getIntNTy(BLOCK_SIZE));
        Value* strm_longint = b.CreateBitCast(genCarryDataLoad(loadIdx-1), b.getIntNTy(BLOCK_SIZE));
        Value* adv_longint = b.CreateOr(b.CreateShl(strm_longint, block_shift), b.CreateLShr(advanceq_longint, BLOCK_SIZE - block_shift), "longadvance");
        result_value = b.CreateBitCast(adv_longint, mBitBlockType);
    }
    // copy entries from previous blocks forward
    for (int i = loadIdx; i > storeIdx; i--) {
        genCarryDataStore(genCarryDataLoad(i-1), i);
    }
    genCarryDataStore(strm_value, storeIdx);
    return result_value;
}
    
void PabloCompiler::SetOutputValue(Value * marker, const unsigned index) {
    IRBuilder<> b(mBasicBlock);
    if (marker->getType()->isPointerTy()) {
        marker = b.CreateAlignedLoad(marker, BLOCK_SIZE/8, false);
    }
    Value* indices[] = {b.getInt64(0), b.getInt32(index)};
    Value* gep = b.CreateGEP(mOutputAddrPtr, indices);
    b.CreateAlignedStore(marker, gep, BLOCK_SIZE/8, false);
}

